template<typename T>
ostream& operator<<(ostream& out, const vector<T>& v)
{
	out << '[';
	bool first = true;
	for (const auto& x : v) {
		if (!first)
			out << ',';
		out << x;
		first = false;
	}
	out << ']';
	return out;
}

class Solution {
private:
	int n;
	vector<int> prefix_sums;
	unordered_map<int, int> result_cache;

	int sum(int begin, int end) {
		return prefix_sums[end] - prefix_sums[begin];
	}

	int helper(int start, int k) {
		if (k == 1)
			return sum(start, n);
		auto [it, inserted] = result_cache.emplace((start << 15) | k, 0);
		if (!inserted)
			return it->second;
		int b = start;
		int e = n - k + 1;
		int result = numeric_limits<int>::max();
		while (b < e) {
			int m = b + (e - b) / 2;
			int sum1 = sum(start, m + 1);
			int sum2 = helper(m + 1, k - 1);
			result = min(result, max(sum1, sum2));
			if (sum1 > sum2) {
				e = m;
			} else {
				b = m + 1;
			}
		}
		it->second = result;
		return result;
	}
public:
	int splitArray(const vector<int>& nums, int k) {
		n = nums.size();
		prefix_sums.resize(n + 1, 0);
		prefix_sums[0] = 0;
		for (int i = 0; i < n; ++i)
			prefix_sums[i + 1] = prefix_sums[i] + nums[i];
		int result = helper(0, k);
		prefix_sums.clear();
		result_cache.clear();
		return result;
	}
};

